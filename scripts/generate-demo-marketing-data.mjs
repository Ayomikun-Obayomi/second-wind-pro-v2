#!/usr/bin/env node
/**
 * Demo marketing UI names from real roster + transfers sheet.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { loadRosterRowsFromFile, loadTransferRowsFromFile } from './roster-sheet.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const STATUSES = ['Negotiating', 'In review', 'Signed'];
const DEAL_TYPES = [
  'Apparel NIL · Regional',
  'Portal · School fit',
  'HS commit package',
  'Regional camp appearances',
];

export function buildDemoMarketingData() {
  const roster = loadRosterRowsFromFile();
  const transfers = loadTransferRowsFromFile();
  const transferBySlug = Object.fromEntries(transfers.map((t) => [t.slug, t]));

  const agentAthletes = {};
  roster.forEach((athlete) => {
    if (!agentAthletes[athlete.agent]) agentAthletes[athlete.agent] = [];
    agentAthletes[athlete.agent].push(athlete.slug);
  });

  const picks = roster.slice(0, 3);
  const deals = picks.map((athlete, i) => {
    const transfer = transferBySlug[athlete.slug];
    const type = transfer
      ? `Transfer portal · ${transfer.toSchool}`
      : DEAL_TYPES[i % DEAL_TYPES.length];
    return {
      slug: athlete.slug,
      title: athlete.name,
      initials: athlete.photo,
      type,
      offer: `$${[420, 310, 185][i]}K`,
      term: `${[2, 1, 3][i]} yr`,
      status: ['Counter sent', 'In review', 'Signed'][i],
      feed: [
        { role: 'Agent', msg: transfer ? `Portal memo filed · ${transfer.fromSchool} → ${transfer.toSchool}` : 'Package terms filed with brand partner' },
        { role: 'Brand', msg: transfer ? 'School fit and deliverables under review' : 'Requested social deliverables breakdown' },
      ],
      agent: athlete.agent,
    };
  });

  const featured = picks[0];
  return {
    deals,
    perfAthlete: featured?.name || 'Represented athlete',
    techAthlete: featured?.name || 'Represented athlete',
    agentAthletes,
  };
}

function serialize(data) {
  return `/* Generated by scripts/generate-demo-marketing-data.mjs — do not edit by hand */
window.DEMO_MARKETING = ${JSON.stringify(data, null, 2)};
`;
}

export function writeDemoMarketingData() {
  const data = buildDemoMarketingData();
  fs.writeFileSync(path.join(root, 'js/demo-marketing-data.js'), serialize(data));
  return data;
}

const isMain = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) {
  const data = writeDemoMarketingData();
  console.log(`Demo marketing data for ${data.deals.map((d) => d.title).join(', ')}`);
}
