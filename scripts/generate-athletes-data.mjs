import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { portraitFocusFromFile, rosterGridFocusFromFile } from './portrait-focus.mjs';
import { fetchRosterRows, loadRosterRowsFromFile, fetchTransferRows, loadTransferRowsFromFile } from './roster-sheet.mjs';
import { fetchAllRosterHeadshots } from './fetch-roster-headshots.mjs';
import { processAllPortraits } from './process-portraits.mjs';
import { SCHOOL_LOGO_SRC } from './school-logos.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

function schoolAbbr(school) {
  return school
    .split(/\s+/)
    .filter((word) => !/^(of|the|university|state|college)$/i.test(word))
    .map((word) => word[0])
    .join('')
    .slice(0, 4)
    .toUpperCase();
}

function defaultBio(entry) {
  const first = entry.name.split(/\s+/)[0];
  return `${first} is a represented football athlete at ${entry.school}, based in ${entry.hometown}.`;
}

const PHOTO_POSITION_OVERRIDES = {
  'jaiden-allos': 'center 0%',
  'sean-brady': 'center 75%',
};

const PHOTO_POSITION_ROSTER_OVERRIDES = {
  'jaiden-allos': 'center 0%',
  'salahadin-allah': 'center top',
  'quory-ambrose': 'center top',
  'nick-herman': 'center top',
  'shacre-colwell': 'center top',
  'malik-diaby': 'center top',
  'maddox-arnold': 'center top',
  'wynden-ho-ohuli': 'center top',
  'sean-brady': 'center 75%',
};

function attachPortraitMeta(slug, data) {
  const portraitPath = path.join(root, 'assets/portraits/athletes', `${slug}.jpg`);
  if (PHOTO_POSITION_OVERRIDES[slug]) {
    data.photoPosition = PHOTO_POSITION_OVERRIDES[slug];
  } else if (fs.existsSync(portraitPath)) {
    data.photoPosition = portraitFocusFromFile(portraitPath);
  }

  if (PHOTO_POSITION_ROSTER_OVERRIDES[slug]) {
    data.photoPositionRoster = PHOTO_POSITION_ROSTER_OVERRIDES[slug];
  } else if (fs.existsSync(portraitPath)) {
    data.photoPositionRoster = rosterGridFocusFromFile(portraitPath);
  }

  return data;
}

function schoolBrand(schoolName, fallbackLogo) {
  let h = 0;
  for (let i = 0; i < schoolName.length; i += 1) h = (h * 31 + schoolName.charCodeAt(i)) >>> 0;
  const brand = {
    name: schoolName,
    logo: schoolAbbr(schoolName) || fallbackLogo,
    color: `hsl(${h % 360} 42% 34%)`,
  };
  if (SCHOOL_LOGO_SRC[schoolName]) brand.logoSrc = SCHOOL_LOGO_SRC[schoolName];
  return brand;
}

function buildAthleteFromRow(entry, transferBySlug) {
  const slug = entry.slug;
  const city = entry.hometown.split(',')[0]?.trim() || 'Local';
  const schoolCode = schoolAbbr(entry.school);
  const transfer = transferBySlug?.[slug];

  const from = transfer?.fromSchool
    ? schoolBrand(transfer.fromSchool, entry.photo)
    : { name: `${city} area`, logo: entry.photo, color: '#3C096C' };
  const to = transfer?.toSchool
    ? schoolBrand(transfer.toSchool, schoolCode)
    : schoolBrand(entry.school, schoolCode);

  return {
    slug,
    data: attachPortraitMeta(slug, {
      name: entry.name,
      position: entry.positionLine,
      photo: entry.photo,
      basketball: false,
      agent: entry.agent,
      athleticLevel: entry.athleticLevel,
      hometown: entry.hometown,
      university: entry.school,
      presentability: 'Represented athlete with partnership-ready media presence.',
      bio: defaultBio(entry),
      accomplishments: [
        `${entry.year} · ${entry.position}`,
        `Represented at ${entry.school}`,
        `Based in ${entry.hometown}`,
      ],
      stats: [
        [entry.position, 'Position'],
        [entry.year, 'Class'],
        [schoolCode, 'Program'],
        ['Represented', 'Status'],
        [entry.agent.split(' ')[0], 'Agent'],
        ['Football', 'Sport'],
      ],
      status: 'represented',
      from,
      to,
      signed: transfer?.window ? `January ${transfer.window}` : undefined,
      photoSrc: `assets/portraits/athletes/${slug}.jpg`,
    }),
  };
}

export async function buildAthletesCatalog() {
  const rows = await fetchRosterRows();
  let transfers = [];
  try {
    transfers = await fetchTransferRows();
  } catch {
    transfers = loadTransferRowsFromFile();
  }
  return catalogFromRows(rows, transfers);
}

export function buildAthletesCatalogSync() {
  const rows = loadRosterRowsFromFile();
  const transfers = loadTransferRowsFromFile();
  return catalogFromRows(rows, transfers);
}

function catalogFromRows(rows, transfers = []) {
  const transferBySlug = Object.fromEntries(transfers.map((t) => [t.slug, t]));
  const athletes = {};
  rows.forEach((entry) => {
    const { slug, data } = buildAthleteFromRow(entry, transferBySlug);
    athletes[slug] = data;
  });
  return athletes;
}

function buildAthleteOrder(catalog) {
  return Object.keys(catalog);
}

function serializeAthletes(catalog) {
  const orderJson = JSON.stringify(buildAthleteOrder(catalog), null, 2);
  const json = JSON.stringify(catalog, null, 2);
  return `/* Generated by scripts/generate-athletes-data.mjs — do not edit by hand */\nconst ATHLETE_ORDER = ${orderJson};\nconst ATHLETES = ${json};\nif (typeof window !== 'undefined') {\n  window.ATHLETE_ORDER = ATHLETE_ORDER;\n  window.ATHLETES = ATHLETES;\n}\n`;
}

function rosterCardHtml(slug, a) {
  return `      <article class="athlete-card" data-athlete="${slug}" data-sport="football">
        <div class="athlete-photo">${a.photo}<span class="sport-tag">Football</span></div>
        <div class="athlete-card-body">
          <div class="athlete-card-head">
            <h3 class="athlete-name">${a.name}</h3>
            <p class="athlete-rank"></p>
            <p class="athlete-card-desc"><span class="athlete-card-desc-text"></span></p>
          </div>
          <a href="/athlete/${slug}" class="partner-btn athlete-read-more"><span class="btn-label">Read more</span></a>
        </div>
      </article>`;
}

const CLASS_ABBR = [
  [/redh?shirt\s+freshman/i, 'RS Fr'],
  [/redh?shirt\s+sophomore/i, 'RS So'],
  [/redh?shirt\s+junior/i, 'RS Jr'],
  [/redh?shirt\s+senior/i, 'RS Sr'],
  [/^freshman$/i, 'Fr'],
  [/^sophomore$/i, 'So'],
  [/^junior$/i, 'Jr'],
  [/^senior$/i, 'Sr'],
];

function abbreviateClass(klass) {
  const raw = String(klass || '').trim();
  if (!raw) return '';
  for (const [re, abbr] of CLASS_ABBR) {
    if (re.test(raw)) return abbr;
  }
  return raw;
}

function carouselMeta(a) {
  const team = a.university || a.to?.name || '';
  const parts = String(a.position || '').split('·').map((s) => s.trim()).filter(Boolean);
  const role = parts[0] || '';
  const klass = abbreviateClass(parts[1] || '');
  // ESPN-style hierarchy under the name: team → class → position
  return { team, klass, role };
}

function carouselCardHtml(slug, a) {
  const { team, klass, role } = carouselMeta(a);
  const meta = [team, klass, role].filter(Boolean).join(' · ');
  return `      <article class="athlete-card" data-athlete="${slug}" data-sport="football">
        <div class="athlete-photo">${a.photo}<span class="sport-tag">Football</span></div>
        <div class="athlete-info">
          <div class="athlete-card-header">
            <h3>${a.name}</h3>
            ${meta ? `<p class="athlete-meta">${meta}</p>` : ''}
          </div>
          <a href="get-started?athlete=${slug}&interest=brand" class="partner-btn">Partner with this athlete</a>
        </div>
      </article>`;
}

function rosterTabsHtml(totalCount, footballCount) {
  return `<button type="button" class="tab active" aria-pressed="true" data-filter="all">All <span class="count">${totalCount}</span></button>
        <button type="button" class="tab" aria-pressed="false" data-filter="football">Football <span class="count">${footballCount}</span></button>`;
}

async function writeAthletesOutput() {
  const rows = await fetchRosterRows();
  const { results: portraitResults } = await fetchAllRosterHeadshots({ force: false });
  const portraitCount = portraitResults.filter((r) => r.status === 'ok' || r.status === 'skip').length;
  await processAllPortraits({ force: false });

  let transfers = [];
  try {
    transfers = await fetchTransferRows();
  } catch {
    transfers = loadTransferRowsFromFile();
  }
  const catalog = catalogFromRows(rows, transfers);

  const footballCount = Object.values(catalog).filter((a) => !a.basketball).length;
  const totalCount = Object.keys(catalog).length;
  const ordered = buildAthleteOrder(catalog).map((slug) => [slug, catalog[slug]]);

  const gridHtml = ordered.map(([slug, a]) => rosterCardHtml(slug, a)).join('\n\n');
  const carouselHtml = ordered.map(([slug, a]) => carouselCardHtml(slug, a)).join('\n\n');
  const tabsHtml = rosterTabsHtml(totalCount, footballCount);

  fs.writeFileSync(path.join(root, 'js/athletes-data.js'), serializeAthletes(catalog));

  const meetPath = path.join(root, 'meet-the-athletes.html');
  let meetHtml = fs.readFileSync(meetPath, 'utf8');
  meetHtml = meetHtml.replace(
    /<div class="roster-tabs" role="group" aria-label="Filter roster by sport">[\s\S]*?<\/div>/,
    `<div class="roster-tabs" role="group" aria-label="Filter roster by sport">\n        ${tabsHtml}\n      </div>`
  );
  meetHtml = meetHtml.replace(
    /<div class="roster-grid" id="roster-grid">[\s\S]*?<\/div>\n    <\/div>/,
    `<div class="roster-grid" id="roster-grid">\n${gridHtml}\n    </div>\n    </div>`
  );
  meetHtml = meetHtml.replace(
    /Explore represented talent across football and basketball[\s\S]*?partnership decisions\./,
    'Explore represented college football talent—with school, class year, position, hometown, and agent surfaced for faster partnership decisions.'
  );
  fs.writeFileSync(meetPath, meetHtml);

  const indexPath = path.join(root, 'index.html');
  let indexHtml = fs.readFileSync(indexPath, 'utf8');
  indexHtml = indexHtml.replace(
    /<div class="roster-tabs" role="group" aria-label="Filter roster by sport">[\s\S]*?<\/div>/,
    `<div class="roster-tabs" role="group" aria-label="Filter roster by sport">\n        ${tabsHtml}\n      </div>`
  );
  indexHtml = indexHtml.replace(
    /Vetted, represented, and actively monetized across football and basketball\./,
    'Vetted, represented, and actively monetized across college football.'
  );
  indexHtml = indexHtml.replace(
    /<div class="roster-track">[\s\S]*?<\/div>\s*<\/div>\s*<\/div>\s*\n\s*<div class="roster-controls">/,
    `<div class="roster-track">\n${carouselHtml}\n        </div>\n      </div>\n    </div>\n\n    <div class="roster-controls">`
  );

  const rosterSectionMatch = indexHtml.match(/<!-- ROSTER -->[\s\S]*?(?=<!-- LATEST MOVEMENT -->)/);
  if (rosterSectionMatch) {
    fs.writeFileSync(
      path.join(__dirname, 'snippets/roster-landing.html'),
      rosterSectionMatch[0].trim()
    );
  }
  fs.writeFileSync(indexPath, indexHtml);

  console.log(`Generated ${totalCount} athletes (${footballCount} football, 0 basketball)`);
  console.log(`Downloaded ${portraitCount} roster headshots from athletics sites and recruiting profiles`);
}

const isMain = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) {
  writeAthletesOutput().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}
